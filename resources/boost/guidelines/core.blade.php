## Laravel Cross Eloquent Search

- `protonemedia/laravel-cross-eloquent-search` searches across multiple Eloquent models in a single query, with support for full-text search, pagination, and relevance ordering.
- Always activate the `laravel-cross-eloquent-search-development` skill when working with cross-model search, or any code that uses the `Search` facade or `ProtoneMedia\LaravelCrossEloquentSearch` classes.
