## Cross Eloquent Search

- `protonemedia/laravel-cross-eloquent-search` searches through multiple Eloquent models simultaneously, with support for pagination, sorting, full-text search, and relationship queries.
- Always activate the `cross-eloquent-search-development` skill when working with multi-model search, cross-table querying, or any code that uses the `Search` facade or `Searcher` class from this package.
